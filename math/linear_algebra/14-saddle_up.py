#!/usr/bin/env python3
""" matrix multiplication """
import numpy as np


def np_matmul(mat1, mat2):
    """_summary_

    Args:
        mat1 (numpy.ndarray): numpy array
        mat2 (numpy.ndarray): numpy array

    Returns:
        numpy.ndarray:the multiplication of the matrix _
    """
    return np.matmul(mat1, mat2)
